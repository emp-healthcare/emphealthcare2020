    private void draftListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_draftListValueChanged
        // TODO add your handling code here:
        String selectedMail=draftList.getSelectedValue();
        
        //Lire le fichier et ajoute le dans la fenetre de lecture
         BufferedReader in = null;
         if(selectedMail!=null)
        try {
            in = new BufferedReader(new FileReader("Ressources/draft/" + selectedMail + ".txt"));
            String line;
            int i=0;
            while ((line = in.readLine()) != null) {
                if(i==0){
                    text1.setText("");
                }
                else if(i==1){
                    dest1.setText(line);
                }
                else if(i==2){
                    subject1.setText(line);
                }
                else {
                    text1.append(line);
                    text1.append("\n");
                }
                i++;
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(DoctorContact.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(DoctorContact.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_draftListValueChanged

